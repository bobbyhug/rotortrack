import { chromium } from "playwright";
const URL = "https://rotortrack.vercel.app/?mock=1";
const b = await chromium.launch({ args:["--use-gl=angle","--use-angle=swiftshader","--enable-unsafe-swiftshader","--ignore-gpu-blocklist"] });
const p = await (await b.newContext({ viewport:{width:600,height:600}, deviceScaleFactor:2 })).newPage();
const errs=[]; p.on("console",m=>m.type()==="error"&&errs.push(m.text())); p.on("pageerror",e=>errs.push("PAGEERR "+e.message));
await p.goto(URL,{waitUntil:"load"});
const ack=p.getByRole("button",{name:/I understand/i}); if(await ack.count()) await ack.click();
await p.waitForTimeout(700);
const got=p.getByRole("button",{name:/Got it/i}); if(await got.count()) await got.click();
await p.waitForTimeout(6000);
await p.screenshot({ path:"/tmp/dep-flying.png" });
await p.getByRole("button",{name:/DIRECT TO/i}).first().click();
await p.waitForTimeout(900);
await p.screenshot({ path:"/tmp/dep-picker.png" });
const close=p.getByRole("button",{name:/Close/i}); if(await close.count()) await close.click();
await p.waitForTimeout(500);
await p.keyboard.press("ArrowRight"); await p.waitForTimeout(700);
await p.screenshot({ path:"/tmp/dep-focus.png" });
console.log("ERRORS:", errs.length? errs.join("\n"):"none");
await b.close();
